    <p>Partajarea de mașină (în engleză carpooling, ride-sharing sau covoiturage) reprezintă împărțirea călătoriei cu autovehiculul astfel încât într-o mașină să călătorească mai mult de o persoană.</p>
    <p>
        Prin utilizarea unei mașini de către mai multe persoane în același timp, partajarea de mașină reduce costurile de călătorie ale fiecărei persoane precum costul benzinei sau taxa de autostradă. Partajarea de mașină este percepută și ca o modalitate mai prietenoasă cu mediul și mai sustenabilă de a călători. Împărțirea unei mașini de mai multe persoane în același timp reduce emisiile de noxe, congestionarea traficului și necesitatea locurilor de parcare.
    </p>
    <p>
        Sistemul este foarte simplu: pleci cu masina personala intr-o anumita directie, la munca, in alt oras sau chiar in alta tara, in vacanta, si pentru a reduce cheltuielile dai de stire online, pe site-uri specializate ca poti lua 1-3 persoane cu tine. Cine este interesat contra cost poate sa mearga si el in aceeasi directie sau poate sa fie lasat in drum spre destinatia soferului. Care este diferanta fata de taxi sau transportul public (in comun)? Proprietarul masinii decide traseul, destinatia, pretul calatoriei.
